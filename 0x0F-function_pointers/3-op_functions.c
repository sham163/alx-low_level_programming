#include "3-calc.h"

/**
 * Description: op_add - sum two integers num
 * @a: first num
 * @b: second num
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * Description: op_sub - sub two integers num
 * @a: first num
 * @b: second num
 * Return: sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * Description: op_mul - sup two integers num
 * @a: first num
 * @b: second num
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * Description: op_div - sup two integers num
 * @a: first num
 * @b: second num
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * Description: op_mod - sup two integers num
 * @a: first num
 * @b: second num
 * Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
